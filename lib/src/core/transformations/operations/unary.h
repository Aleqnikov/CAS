#ifndef UNARY_H
#define UNARY_H

#include "../mapping.h"

/**
 * В данном файле объявлен  интерфейс любой унарной операции
 * при создании любой операции, необходимо наследоваться от него, это необходимо
 * для универсальной типизации СКА. Предполагается, что унарная операция, это преобразование
 * множества в себя, поэтому на вход она получает  объект одного типа, и возвращяет на 
 * выход новый объект того же типа. 
 */



/**
 * @brief Базовый класс-шаблон для реализации унарных операций через статический полиморфизм (CRTP).
 * * Является частным случаем отображения множества в себя (эндоморфизм).
 * Принимает класс-реализацию и тип данных, который одновременно является областью 
 * определения и областью значений операции.
 * * @tparam Derived Класс-реализация конкретной унарной операции (например, Negate, Abs).
 * @tparam T Тип операнда и результата операции.
 * * @par Пример реализации и использования:
 * @code
 * template<typename Type>
 * class Negate : public UnaryOperation<Negate<Type>, Type> {
 * public:
 * static Type calc(Type a) { return -a; }
 * };
 * * auto res = Negate<int>::execute(10); // Результат: -10
 * @endcode
 */
template<typename Derived, typename T>
class UnaryOperation : public Mapping<Derived, T, T> {};

#endif //UNARY_H